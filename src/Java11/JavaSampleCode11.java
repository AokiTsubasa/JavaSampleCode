package Java11;

/**
 *  1-1 変数を使って文字「Hello Java World !!」を表示する
 */
public class JavaSampleCode11 {

    public static void main(String[] args) {
        
        //「Hello Java World!!」を変数wordに入れる
        String word = "Hello Java World!!";
        
        //変数に入れた「Hello Java World!!」を表示する
        System.out.println(word);
        
    }
    
}